المادة الأولى : المقدمة :  

هذه البوابة الالكترونية لشركة أبوذياب لتأجير السيارات بالمملكة العربية السعودية والتي يشار اليها فيما بعد بتطبيق أبوذياب متاحة للاستخدام من قبل الشركات، ويخضع دخول العميل واستخدامه  لهذه البوابة لبنود وشروط الاستخدام بالتطبيق ولانظمة المملكة العربية السعودية وبذلك يعد دخوله بمثابة موافقة دون قيد أو شرط على بنود وشروط الاستخدام لهذه البوابة سواء كان مستخدم التطبيق  مسجلا أو لم يكون مسجل في التطبيق , وتسري هذه الموافقة اعتبارا من تاريخ أول استخدام له لهذا التطبيق ، ويصبح أي تعديل لهذه البنود والشروط نافذا فور القيام بالتعديل مالم يتم بيان خلاف ذلك من قبل العميل ، ويعني استمرار العميل باستخدام هذه البوابة بعد أي تعديل قبوله التام لهذه التعديل .                                                                                                                                                  

  

