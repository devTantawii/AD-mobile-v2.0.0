تقوم الشركة بمتابعة التطبيق ومراجعنه على مدار اليوم لتوفير المعلومات للعميل عن خدماتها كذلك يقوم التطبيق بتسهيل عملية الاتصال ما بين العملاء والشركة بخصوص أي خدمات أو ملاحظات قد تكون محط اهتمام العميل . 

أن الخدمات والمعلومات الواردة في هذا التطبيق موجهه ومتوفرة للتداول في نطاق المملكة العربية السعودية ولقد تم تقديم هذه الخدمات بموجب قوانين وأنظمة المملكة العربية السعودية ذات العلاقة المتخصصة في تقديم مثل هذه الخدمات وبناء عليه قد لا يكون العميل مؤهلا للاستفادة من جميع الخدمات الواردة وتحتفظ الشركة بالحق في رفض أي طلب في أي وقت دون اشعار العميل بذلك ودون تحمل أي مسؤولية قانونية جراء تكبد أي خسارة ماليه نتيجة ارسال مثل هذا الطلب من قبله للشركة .                                                          

  

المادة الخامسة: الية التسجيل 

عند التسجيل في موقع الشركة سيطلب من العميل تدوين اسم المستخدم وكلمة المرور التي سوف يتم تزويد العميل بها من قبل إدارة الشركة وسوف يتم ارسال رمز التفعيل له عند الدخول الى التطبيق عن طريق الجوال المسجل والمعتمد لدى شركة أبوذياب للعميل.                  

علما بأنه عند قيام العميل بإدخال هذه المعلومات ورمز التفعيل المرسل من قبل الشركة لهاتفه الجوال المعتمد لدى شركة أبوذياب في التطبيق يعتبر بمثابة توقيعا الكترونيا وقبولا للتعاملات الالكترونية والموافقة عليها بالإضافة الى قبول شروط واحكام استخدام هذا التطبيق المذكورة في البنود السابقة واللاحقة في هذه الاتفاقية .                                                                                                         

  

المادة السادسة : المعلومات المرسلة لمواقع الشركة :  

ان اي معلومات او مواد ترسل للشركة عبر شبكة الانترنت من خلال التطبيق هذا تصبح ملكا لشركة أبوذياب لتاجير السيارات ومن المعلوم ان شبكة الانترنت ما زالت تفتقر الى الامن الشامل . وبالتالي فان مسئولية اي معلومات عبر شبكة الانترنت تقتصر على العميل وحده من يتحمل المخاطر المتعلقة بذلك .  

المادة السابعة : الضمان :  

لقد قامت شركة أبوذياب لتاجير السيارات باتخاذ كافة التدابير المناسبة لوضع المعلومات في هذا الموقع يشكل امان  وستحاول العمل على تحديث هذه المعلومات اولاً باول ومع هذا فان الشركة لا تمنح اي ضمانات صريحة او ضمنية بخصوص دقة المعلومات الورادة قبل تقديم الطلب الرسمي وتوقيع الاتفاقية ، لذا وجب علي العميل ان يتاكد دائما من ان العروض والاسعار الخاصة بالسيارات والخدمات والشروط والاحكام الوردة في الموقع لازالت سارية المفعول اذ ان الشركة تحتفظ بالحق في تغييرها في اي وقت ودون اشعار سابق للعميل لذا وجب التنوية بضرورة مراقبة الاسعار والخدمات المقدمة من حين الى اخر .                                                                            

المادة / الثامنة : اخلاء المسئولية :  

مهما كان الحال او الظرف ، فان الشركة غير مسئولة تجاة اي من الامور التالية علي سبيل المثال لا الحصر : الاهمال الذي يتسبب في اية اضرار او تلفيات من اي نوع سواء كانت مباشرة او عارضة او خاصة او لاحقة ، او اي مصاريف او خسائر قد تنجم بسبب استخدام العميل لهذا الموقع او عدم القدرة على استخدامة اياه ، او وقوع بعض الاخطاء او السهو أو تاخير استجابة النظام ( بسبب خلل في انظمة التشغيل او الاتصال ) او اعاقة في التشغيل او وقوع اعطال او تعرض اجهزة الكمبيوتر للفيروسات او تعطل النظام بالكامل ، او خسارة اي ارباح او تعرض سمعة العميل للاساءة حتى لو جرى الاشعار صراحة باحتمالية وقوع مثل هذه الامور ، او وقوع مشاكل جراء الوصول الى الموقع او الدخول فيه او استخدامة او استظهاره او الوصول من خلاله الى مواقع اخرى .                                       

المادة التاسعة : الخصوصية :  

تدرك الشركة حاجة العميل لمعرفة كيف تتعامل مع المعلومات الشخصية الخاصة به ، كما تدرك حجم القلق الذي يراود العملاء  من ناحية خصوصية وسرية وامن المعلومات التي يزودها بها العميل عبر  شبكة الانترنت  وتطمئن الشركة العميل بهذا الخصوص بانها ملتزمة بالحفاظ على تلك المعلومات في غاية الخصوصية والسرية ان هذا التصريح يمثل سياسة الشركة الحالية بشان السرية والخصوصية , وان استخدام العميل لتطبيق أبوذياب ومواقع الشركة الاخرى يعني قبولة للاحكام والشروط وايضا لتصريح السرية والخصوصية هذا .              

المادة العاشرة : شروط تقديم الخدمة والاسعار : 

ان استخدام العميل للموقع وطلبة للخدمات مقرون بالعلم الاكيد بتكاليف تلك الخدمات ورسومها وكافة المعلومات المتوفرة حولها سواء داخل موقع الشركة او خارجة مع عدم ضمان العروض والاسعار المتوفرة اذ ان الشركة تحتفظ بالحق في تغييرها دون اشعار مسبق ودون ادنى مسؤولية ، مالم يتم الاتفاق وكتابة العقود .  

كذلك فان استخدام العميل للموقع يعني قبولة للفواتير الالكترونية والتعامل معها فيما يخص السداد لدى البنوك والسداد الالكتروني ولا تتحمل شركة المنفتاح اي مسؤولية او مطالبات مالية نتجة ذلك . 

يجب على العميل الاطلاع على اسعار السيارات المعروضة وقيمة الخدمة المقدمة من الشركة والتاكد من صحتها ، وعند وجود خطا فيحق للعميل الاعتراض لدى الشركة وتكون مدة قبول الاعتراض على صحة بيانات الفاتورة الالكتونية مدة (30 ) يوما من تاريخ صدورها وسجلات الشركة  الخاصة باسعار الخدمات وقت طلب الخدمة والاتفاق عليه هي المرجع الملزم للطرفين .                                

المادة الحادى عشر : التغيرات والتحديثات :  

تملك الشركة الحق في تغيير هذه الشروط والاحكام او استبدالها كليا بشروط واحكام اخرى جديدة أو استبدال صفحة او صفحات ، واردة في مواقعها علي شبكة الانترنت دون اشعار بذلك ، كما ان استمرارية دخول العميل للموقع الشركة او استخدمة يعتبر بمثابة موافقة منه على التغييرات التي جرت على الموقع كما اشير سلفاً . كذلك تملك الشركة الحق في تعديل كامل الموقع او جزء منه او ايقافة او تعليقة مؤقتا أو بشكل دائم دون اشعار مسبق وفي اي وقت تراه . كما ان العميل موافق ايضا على ان الشركة غير مسئولة تجاهه او تجاه اي اطراف اخرى عن اي تعديل للموقع او ايقافة او تعليقة .                                                                                            

كما ان اي تعديل على الشروط والاحكام ينصرف على جميع مواقع الشركة وفي كل الخدمات التي تقدمها .  

المادة الثانية عشر : التزامات العميل الامنية  

يتعين على العميل حفظ الرقم السري الخاص به في مكان امن وسري في كافة الاوقات واتخاذ الخطوات التي تحول دون استخدامه بشكل غير مفوض . 

ويتعين على العميل ابلاغ الشركة فوراً باي وصول غير مفوض او اي تعليمات غير مفوضة يعلم بها او يشتبه فيها او تروده الشكوك بأن شخصا اخر قد كشف كلمة السر الخاصة به ، وفي هذه الحالة يتعين علية تغيير الرقم السري فوراً وعلية مساعدة الشركة ومساعدة الجهات الامنية في العمل على استرداد الخسائر ان حدثت . وقد تعطي الشركة معلومات عن العميل للشرطة أو لاي طرف اخر اذا كان ذلك يمنع الخسائر او يساعد في استردادها .  

ويتعهد العميل بعدم الحاق الضرر الالكتروني بالتطبيق او الخدمات المقدمة داخل التطبيق او باي مواقع مرتبطه به .  

كما يتعهد التعامل مع اي هكر لاقتحام سرية الموقع او التسبب بالحاق اي ضرر به او للبيانات الموجودة فيه ، كما يتعهد العميل بعدم ارسال اي رسائل متسلسلة تتسبب بضغط شديد على الموقع مما يؤثر على ادائة والحاق أضرار به أو بمستخدميه وبياناتهم .                         

المادة الثالثة عشر : طلبات العميل :  

أ / النتائج المترتبة على طلبات العميل :  

يفوض العميل الشركة بالتصرف بموجب الطلبات التي يبدو ظاهرياً انها طلبات صحيحة حسب الاصول حتي وان كانت متعارضة مع اي طلبات اخرى في اي وقت فيما يتعلق بالخدمات المطلوبة . 

ب / تنفيذ التعليمات :  

سوف يتم تنفيذ طلبات الخدمات الورادة عن طريق التطبيق باستخدام كلمة السر الخاصة بالعميل دائما  .  

ج / إخفاق الطرف الاخر :  

ان الشركة غير مسئولة عن اي إخفاق من قبل الطرف الاخر في تنفيذ التعليمات او اي تاخير او اخطاء من جانب ذلك الطرف عند تنفيذ التعليمات مهما كانت الاسباب . 

ح / دقة واكتمال التعليمات  :  

يجب على العميل التاكد من دقة واكتمال كافة البيانات المطلوبة ولن تقبل اية بيانات ناقصة ويتحمل العميل مسؤوليتة عن الاخطاء الناتجة من عدم اكتمال البيانات .  

سوف تبذل شركة أبوذياب كل مافي وسعها وتسخر كافة طاقاتها لتنفيذ طلبات عملاءها الكرام .  

المادة الرابعة عشر : أحكام عامة :  

أ – القانون المطبق والاختصاص القضائي :  

تخضع هذه الوثيقة لنظام التعاملات الالكترونية ونظام مكافحة الجرائم المعلوماتية ونظام هيئة النقل والمرور وتفسر بموجبها وبهذا يوافق العميل والشركة على احالة اي نزاع او خلاف قد ينشا حول هذه الشروط والاحكام الى الجهة القضائية المختصة .  

ب – المرجع في حال اختلاف سجلات الطرفين :  

في حال اختلاف البيانات المقدمة من كلا الطرفين فان المرجع المنهي للخلاف هي البيانات الواردة في سجلات الشركة وقت التعاقد او تقديم الطلبات . 

ج -يتم خصم 25% من مبلغ الإيجار في حال تم إلغاء الحجز قبل الموعد بست ساعات أو أقل. 

د- يتم خصم مبلغ أول يوم حجز في حال لم يتم الإستلام أو الإلغاء بعد موعد الحجز. 

ذ- فى حال عدم إحضار  الأوراق الثبوتية ( الهوية - رخصة القيادة ) يتم خصم 25% من قيمة إيجار اليوم  الأول . 

ه-فى حال عدم القدرة على تفويض العميل ( رخصة منتهية - إيقاف خدمات - عدم التسجيل أو تحديث البيانات فى نظام أبشر ) يتم خصم قيمة إيجار يوم واحد .  

و- يتم احتساب قيمة الايجار من التاريخ والوقت المحددين من قبل العميل فى الحجز و ليس وقت الاستلام الفعلى فى حالة وصول العميل متأخر و فى حالة وصول العميل مبكرا يتم احتساب وقت الاستلام الفعلى وليس المحدد فى الحجز .  

و- بحسب سياسة بوليصة التأمين و ذلك فيما يتعلق بأعمار المستأجرين فإذا كان عمر العميل مافوق 65 سنة فيجب عليه قبول أحد الشروط أدناه :  

1- أن يدفع ضعف قيمة رسوم التحمل فى حال حدوث حادث - لا قدر الله و ذلك بحسب قيمة رسوم التحمل لنوع السيارة المستأجرة حسب بوليصة شركة التأمين .  

2- أن يكون العقد CDW . 

شروط و أحكام خدمة الإيجار بالساعة : 

 لا يمكن الاستفادة من مميزات العضوية عند طلب الخدمة . 

 الاستلام من فروع المطارات فقط والتسليم في أي فرع . 

