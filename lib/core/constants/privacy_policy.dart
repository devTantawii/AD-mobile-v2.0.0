 الحد الأقصى للخدمة ( 5 ساعات ) وعند تجاوز عدد ساعات الخدمة يتم احتساب قيمة ايجار يوم كامل . 

  

المادة الخامسة عشر : الاقرار بالعلم :  

يقر العميل بانه قرا هذه الشروط والاحكام ويلتزم بها دون قيد او شرط او تحفظ شروط وأحكام استخدام وتسجيل في تطبيق شركة أبوذياب الالكتروني 

المادة الأولى : المقدمة :  

هذه البوابة الالكترونية لشركة أبوذياب لتأجير السيارات بالمملكة العربية السعودية والتي يشار اليها فيما بعد بتطبيق أبوذياب متاحة للاستخدام من قبل الشركات، ويخضع دخول العميل واستخدامه  لهذه البوابة لبنود وشروط الاستخدام بالتطبيق ولانظمة المملكة العربية السعودية وبذلك يعد دخوله بمثابة موافقة دون قيد أو شرط على بنود وشروط الاستخدام لهذه البوابة سواء كان مستخدم التطبيق  مسجلا أو لم يكون مسجل في التطبيق , وتسري هذه الموافقة اعتبارا من تاريخ أول استخدام له لهذا التطبيق ، ويصبح أي تعديل لهذه البنود والشروط نافذا فور القيام بالتعديل مالم يتم بيان خلاف ذلك من قبل العميل ، ويعني استمرار العميل باستخدام هذه البوابة بعد أي تعديل قبوله التام لهذه التعديل .                                                                                                                                                  

  

المادة الثانية: حقوق الملكية الفكرية :  

ان الاسم التجاري للشركة هو شركة أبوذياب لتأجير السيارات المحدودة وعلامتها التجارية والعلامات المسجلة باسمها وعناوين الشركة الالكترونية وكل ما يتضمنه الموقع من مواد ونصوص وصور ورسومات وتعاميم ونماذج وملفات ووسائط متعددة وبرمجيات هي ملك للشركة وتحتفظ بكافة الحقوق الملكية الفكرية وحقوق النشر والتوزيع المتعلقة بها بما كفل لها النظام , ولا يسمح بإعادة طبع هذه المواد أو توزيعها أو تعديلها أو استخدامها لأي أغراض دعائية كانت أو تجارية أو إعادة نشرها بأي شكل دون الحصول على اذن خطي وصريح من شركة أبوذياب لتأجير السيارات .                                                                                                                               

  

المادة الثالثة: طبيعة الموقع 

يعتبر هذا التطبيق هو التطبيق الرئيسي لشركة أبوذياب الموجه الى قطاع الشركات وهو يوفر للزوار الكرام من هذا القطاع المعلومات الأساسية عن الشركة وكافة الخدمات التي تقدمها ورؤيتها في تقديم خدماتها وعروضها وخصوماتها وعرض كل ما يستجد من شانة .       

  

المادة الرابعة: استخدام التطبيق  

تقوم الشركة بمتابعة التطبيق ومراجعنه على مدار اليوم لتوفير المعلومات للعميل عن خدماتها كذلك يقوم التطبيق بتسهيل عملية الاتصال ما بين العملاء والشركة بخصوص أي خدمات أو ملاحظات قد تكون محط اهتمام العميل . 

أن الخدمات والمعلومات الواردة في هذا التطبيق موجهه ومتوفرة للتداول في نطاق المملكة العربية السعودية ولقد تم تقديم هذه الخدمات بموجب قوانين وأنظمة المملكة العربية السعودية ذات العلاقة المتخصصة في تقديم مثل هذه الخدمات وبناء عليه قد لا يكون العميل مؤهلا للاستفادة من جميع الخدمات الواردة وتحتفظ الشركة بالحق في رفض أي طلب في أي وقت دون اشعار العميل بذلك ودون تحمل أي مسؤولية قانونية جراء تكبد أي خسارة ماليه نتيجة ارسال مثل هذا الطلب من قبله للشركة .                                                          

  

المادة الخامسة: الية التسجيل 

عند التسجيل في موقع الشركة سيطلب من العميل تدوين اسم المستخدم وكلمة المرور التي سوف يتم تزويد العميل بها من قبل إدارة الشركة وسوف يتم ارسال رمز التفعيل له عند الدخول الى التطبيق عن طريق الجوال المسجل والمعتمد لدى شركة أبوذياب للعميل.                  

